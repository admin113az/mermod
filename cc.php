<script>
    // Lấy phần tử <ul> chứa danh sách file
    const fileList = document.getElementById("fileList");

    // Chuyển đổi các <li> thành mảng
    const items = Array.from(fileList.getElementsByTagName("li"));

    // Sắp xếp các <li> theo thứ tự chữ cái dựa trên nội dung bên trong thẻ <a>
    items.sort((a, b) => {
        const textA = a.querySelector("a").textContent.trim();
        const textB = b.querySelector("a").textContent.trim();
        return textA.localeCompare(textB);
    });

    // Xóa nội dung hiện tại của <ul>
    fileList.innerHTML = "";

    // Thêm lại các <li> đã được sắp xếp vào <ul>
    items.forEach(item => fileList.appendChild(item));
</script>

